class TortillaMaiz:
    toasted
    temperature
    toasting = 20

    def isToasted():
        return self.toasted

    def getCurrentTemperature():
        return self.temperature

    def getToastTemperature():
        return self.toasting
    
    def setCurrentTemperature(temp):
        self.temperature = temp

    def toast(toasted):
        self.toasted = toasted